USE smartHR;

-- 1. Job_Posting (No dependencies)
CREATE TABLE Job_Posting (
    Job_ID INT PRIMARY KEY AUTO_INCREMENT,
    Job_Title VARCHAR(100),
    Job_Description TEXT,
    Qualifications TEXT,
    Job_Type VARCHAR(50),
    Posted_Date DATE
);

-- 2. Applicant (Depends on Job_Posting)
CREATE TABLE Applicant (
    Applicant_ID INT PRIMARY KEY AUTO_INCREMENT,
    Applied_Job_ID INT,
    Name VARCHAR(100),
    Email VARCHAR(100),
    Experience VARCHAR(100),
    Interview_Scheduled_Status BOOLEAN,
    Interview_Date DATE,
    Interviewer VARCHAR(100),
    FOREIGN KEY (Applied_Job_ID) REFERENCES Job_Posting(Job_ID)
);

-- 3. Department (No dependencies)
CREATE TABLE Department (
    Department_ID INT PRIMARY KEY AUTO_INCREMENT,
    Manager_ID INT,
    Department_Name VARCHAR(100)
);

-- 4. Employee (Depends on Applicant, Department)
CREATE TABLE Employee (
    Employee_ID INT PRIMARY KEY AUTO_INCREMENT,
    Applicant_ID INT,
    Department_ID INT,
    Hired_Salary DECIMAL(10, 2),
    Joining_Date DATE,
    FOREIGN KEY (Applicant_ID) REFERENCES Applicant(Applicant_ID),
    FOREIGN KEY (Department_ID) REFERENCES Department(Department_ID)
);

-- 5. Payroll (Depends on Employee)
CREATE TABLE Payroll (
    Payroll_ID INT PRIMARY KEY AUTO_INCREMENT,
    Employee_ID INT NOT NULL,
    Basic_Pay DECIMAL(10, 2) NOT NULL CHECK (Basic_Pay >= 0),
    Allowances DECIMAL(10, 2) DEFAULT 0 CHECK (Allowances >= 0),
    PF_Deductions DECIMAL(10, 2) DEFAULT 0 CHECK (PF_Deductions >= 0),
    Tax_Deductions DECIMAL(10, 2) DEFAULT 0 CHECK (Tax_Deductions >= 0),
    Total_Bonus DECIMAL(10, 2) DEFAULT 0 CHECK (Total_Bonus >= 0),
    Pay_Date DATE NOT NULL,
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID) ON DELETE CASCADE
);

-- 6. Performance (Depends on Employee)
CREATE TABLE Performance (
    Performance_ID INT PRIMARY KEY AUTO_INCREMENT,
    Employee_ID INT NOT NULL,
    KPIs TEXT NOT NULL,
    Review_Date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    Appraisal INT CHECK (Appraisal BETWEEN 1 AND 5),
    Comments TEXT NOT NULL,
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID) ON DELETE CASCADE
);

-- 7. Leave_Request (Depends on Employee)
CREATE TABLE Leave_Request (
    Leave_ID INT PRIMARY KEY AUTO_INCREMENT,
    Employee_ID INT,
    Start_Date DATE,
    End_Date DATE,
    Reason TEXT,
    Permission_Status VARCHAR(50),
    CHECK (Start_Date < End_Date),
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID)
);

-- 8. Skill (No dependencies)
CREATE TABLE Skill (
    Skill_ID INT PRIMARY KEY AUTO_INCREMENT,
    Skill_Name VARCHAR(100),
    Skill_Description TEXT
);

-- 9. Training (Depends on Skill)
CREATE TABLE Training (
    Training_ID INT PRIMARY KEY AUTO_INCREMENT,
    Skill_ID INT,
    Program_Name VARCHAR(100),
    Start_Date DATE,
    End_Date DATE,
    CHECK (Start_Date < End_Date),
    FOREIGN KEY (Skill_ID) REFERENCES Skill(Skill_ID)
);

-- 10. Exit_Management (Depends on Employee)
CREATE TABLE Exit_Management (
    Exit_ID INT PRIMARY KEY AUTO_INCREMENT,
    Employee_ID INT UNIQUE,
    Resignation_Date DATE,
    Exit_Reason TEXT,
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID)
);

-- 11. Conflict_Management (No dependencies)
CREATE TABLE Conflict_Management (
    Conflict_ID INT PRIMARY KEY AUTO_INCREMENT,
    Conflict_Type VARCHAR(255) NOT NULL,
    Status ENUM('Resolved', 'Ongoing', 'Pending') DEFAULT 'Pending',
    Reported_Date TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 12. Conflict_Employees (Depends on Conflict_Management, Employee)
CREATE TABLE Conflict_Employees (
    Conflict_Employee_ID INT PRIMARY KEY AUTO_INCREMENT,
    Conflict_ID INT NOT NULL,
    Employee_ID INT NOT NULL,
    FOREIGN KEY (Conflict_ID) REFERENCES Conflict_Management(Conflict_ID) ON DELETE CASCADE,
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID) ON DELETE CASCADE
);

-- 13. Engagement_Activities (No dependencies)
CREATE TABLE Engagement_Activities (
    Activity_ID INT PRIMARY KEY AUTO_INCREMENT,
    Activity_Name VARCHAR(255) NOT NULL,
    Description TEXT NOT NULL,
    Activity_Type ENUM('Workshop', 'Team Building', 'Wellness Session', 'CSR Activity', 'Training Program') NOT NULL,
    Activity_Date DATE NOT NULL,
    Status ENUM('Conducted', 'Cancelled', 'Yet to be Conducted') DEFAULT 'Yet to be Conducted',
    Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 14. Surveys (No dependencies)
CREATE TABLE Surveys (
    Survey_ID INT PRIMARY KEY AUTO_INCREMENT,
    Survey_Title VARCHAR(255) NOT NULL,
    Description TEXT NOT NULL,
    Survey_Type ENUM('Employee Feedback', 'Training Evaluation', 'HR Policy Review', 'Culture & Engagement', 'Wellness Assessment') NOT NULL,
    Expiry_Date DATE NOT NULL,
    Status ENUM('Active', 'Closed', 'Draft') DEFAULT 'Draft',
    Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 15. Goals (No dependencies)
CREATE TABLE Goals (
    Goal_ID INT PRIMARY KEY AUTO_INCREMENT,
    Title VARCHAR(255) NOT NULL,
    Description TEXT NOT NULL,
    Deadline DATE NOT NULL,
    Status ENUM('Not Started', 'In Progress', 'Completed') NOT NULL,
    Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 16. Compliance_Policies (No dependencies)
CREATE TABLE Compliance_Policies (
    Policy_ID INT PRIMARY KEY AUTO_INCREMENT,
    Policy_Title VARCHAR(255) NOT NULL UNIQUE,
    Description TEXT NOT NULL,
    Effective_Date DATE NOT NULL,
    Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


-- 17. Courses (No dependencies)
CREATE TABLE Courses (
    Course_ID INT PRIMARY KEY AUTO_INCREMENT,
    Course_Title VARCHAR(255) NOT NULL,
    Course_Description TEXT NOT NULL,
    Instructor VARCHAR(255) NOT NULL,
    Category VARCHAR(100) NOT NULL,
    Duration VARCHAR(50) NOT NULL,
    Created_Date TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 18. Employee_Training (Depends on Employee, Courses)
CREATE TABLE Employee_Training (
    Employee_Training_ID INT PRIMARY KEY AUTO_INCREMENT,
    Employee_ID INT NOT NULL,
    Course_ID INT NOT NULL,
    Completion_Status ENUM('Not Started', 'In Progress', 'Completed') DEFAULT 'Not Started',
    Completion_Date DATE DEFAULT NULL,
    Score DECIMAL(5,2) DEFAULT NULL,
    Progress VARCHAR(50) DEFAULT '0%',
    Feedback TEXT DEFAULT NULL,
    Enrolled_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID) ON DELETE CASCADE,
    FOREIGN KEY (Course_ID) REFERENCES Courses(Course_ID) ON DELETE CASCADE
);

-- 19.Users Table
CREATE TABLE users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE,
    role ENUM('admin', 'payroll', 'manager', 'employee', 'hr', 'training', 'compliance', 'it', 'finance', 'executive') DEFAULT NULL,
    department VARCHAR(50) DEFAULT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

